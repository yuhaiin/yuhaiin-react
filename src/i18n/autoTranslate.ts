export function translatePhrase(text: string, language: SupportedLanguage): string | undefined {
    if (language === 'en') return undefined;
    const table = language === 'ja' ? ja : ko;
    return table[text];
}

export function translateInterpolatedText(text: string, language: SupportedLanguage): string | undefined {
    if (language === 'en') return undefined;

    const blocked = text.match(/^Displaying (\d+) connections denied by rules$/);
    if (blocked) {
        return language === 'ja'
            ? `ルールにより拒否された接続を ${blocked[1]} 件表示中`
            : `규칙에 의해 거부된 연결 ${blocked[1]}개 표시 중`;
    }

    const blocks = text.match(/^(\d+) Blocks$/);
    if (blocks) {
        return language === 'ja'
            ? `${blocks[1]} ブロック`
            : `${blocks[1]} 차단`;
    }

    const activeInbounds = text.match(/^(\d+) active inbounds$/);
    if (activeInbounds) {
        return language === 'ja'
            ? `${activeInbounds[1]} 件の有効なインバウンド`
            : `활성 인바운드 ${activeInbounds[1]}개`;
    }

    const inbounds = text.match(/^(\d+) inbounds$/);
    if (inbounds) {
        return language === 'ja'
            ? `${inbounds[1]} 件のインバウンド`
            : `인바운드 ${inbounds[1]}개`;
    }

    const configuredEntryPoints = text.match(/^(\d+) configured entry points$/);
    if (configuredEntryPoints) {
        return language === 'ja'
            ? `${configuredEntryPoints[1]} 件の設定済みエントリーポイント`
            : `설정된 진입점 ${configuredEntryPoints[1]}개`;
    }

    const aliasMirrorNodes = text.match(/^(\d+) alias and mirror nodes defined$/);
    if (aliasMirrorNodes) {
        return language === 'ja'
            ? `${aliasMirrorNodes[1]} 件のエイリアス/ミラーノードを定義済み`
            : `별칭 및 미러 노드 ${aliasMirrorNodes[1]}개 정의됨`;
    }

    const aliasesAndMirrors = text.match(/^(\d+) aliases and mirrors$/);
    if (aliasesAndMirrors) {
        return language === 'ja'
            ? `${aliasesAndMirrors[1]} 件のエイリアスとミラー`
            : `별칭 및 미러 ${aliasesAndMirrors[1]}개`;
    }

    const listsAvailable = text.match(/^(\d+) [Ll]ists [Aa]vailable$/);
    if (listsAvailable) {
        return language === 'ja'
            ? `${listsAvailable[1]} 件のリストが利用可能`
            : `사용 가능한 목록 ${listsAvailable[1]}개`;
    }

    const hours = text.match(/^([\d,.]+)\s+Hours$/);
    if (hours) {
        return language === 'ja'
            ? `${hours[1]} 時間`
            : `${hours[1]} 시간`;
    }

    const entries = text.match(/^(\d+)\s+Entries$/);
    if (entries) {
        return language === 'ja'
            ? `${entries[1]} エントリ`
            : `${entries[1]}개 항목`;
    }

    const entriesLower = text.match(/^(\d+)\s+entries$/);
    if (entriesLower) {
        return language === 'ja'
            ? `${entriesLower[1]} エントリ`
            : `${entriesLower[1]}개 항목`;
    }

    const steps = text.match(/^(\d+)\s+steps$/);
    if (steps) {
        return language === 'ja'
            ? `${steps[1]} ステップ`
            : `${steps[1]}단계`;
    }

    const addNew = text.match(/^Add new (.+?)( \(one per line\))?$/);
    if (addNew) {
        const translatedTitle = translatePhrase(addNew[1], language);
        if (translatedTitle) {
            if (language === 'ja') {
                return `新しい ${translatedTitle} を追加${addNew[2] ? ' (1 行に 1 件)' : ''}`;
            }
            return `새 ${translatedTitle} 추가${addNew[2] ? ' (한 줄에 하나)' : ''}`;
        }
    }

    const noEntries = text.match(/^No (.+) entries yet\.$/);
    if (noEntries) {
        const translatedTitle = translatePhrase(noEntries[1], language);
        if (translatedTitle) {
            return language === 'ja'
                ? `${translatedTitle} エントリはまだありません。`
                : `아직 ${translatedTitle} 항목이 없습니다.`;
        }
    }

    const lastSynced = text.match(/^Last Synced:\s*(.+)$/);
    if (lastSynced) {
        return language === 'ja'
            ? `最終同期: ${lastSynced[1]}`
            : `마지막 동기화: ${lastSynced[1]}`;
    }

    const titleWithDescription = text.match(/^(.+) - (.+)$/);
    if (titleWithDescription) {
        const translatedTitle = translatePhrase(titleWithDescription[1], language);
        const translatedDescription = translatePhrase(titleWithDescription[2], language);
        if (translatedTitle || translatedDescription) {
            return `${translatedTitle ?? titleWithDescription[1]} - ${translatedDescription ?? titleWithDescription[2]}`;
        }
    }

    return translatePhrase(text, language);
}
